# Ejemplo de bucle for
frutas = ["manzana", "banana", "cereza"]
for fruta in frutas:
    if fruta in "banana":
        print("La banana es mi fruta favorita")
        continue
    print(fruta)

# Ejemplo de bucle while
contador = 0
while contador < 5:
    print(contador)
    contador += 1

# Ejemplo de bucle for con range()
for i in range(5):
    print(i)

